function onUse(cid, item, fromPosition, itemEx, toPosition)

local cfg = config[item.actionid]

	if cfg then
		if getPlayerStorageValue(cid, cfg.storage) < 1 then
		
		if item.itemid == 19555 and math.random(1, 100) <= 25 then
			doPlayerAddItem(cid, 19265, 1)
		elseif item.itemid == 19556 and math.random(1, 100) <= 25 then
			doPlayerAddItem(cid, 19266, 1)
		end
		
		if item.itemid == 19554 then
		
			if getGlobalStorageValue(1818180) < 1 and item.actionid == 12028 then
				doPlayerAddItem(cid, 19277, 1)			
				doRemoveItem(item.uid, 1)
				setPlayerStorageValue(cid, 1818180, 1)
				doPlayerBroadcastMessage(cid, "O player "..getCreatureName(cid).." acaba de encontrar um Ovo Dourado.")
			else
				doPlayerSendTextMessage(cid, "Alguém já encontrou esse Ovo dourado.")
			end	
			
		end
		
			setPlayerStorageValue(cid, cfg.storage, 1)		
			setPlayerStorageValue(cid, 1616008, (getPlayerStorageValue(cid, 1616008) + 1))
			doPlayerSendTextMessage(cid, 27, "Você já encontrou ["..getPlayerStorageValue(cid, 1616008).."] ovos.")
		else
			doPlayerSendTextMessage(cid, 27, "Você já coletou esse ovo!")
		end
	end

return true
end