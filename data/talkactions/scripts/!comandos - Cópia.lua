function onSay(cid, words, param, channel)

	doPlayerPopupFYI(cid, [[!pokeballs - Checa as brokes de um pokemon, ex: !pokeballs charmander;
	!teleport ou !h - Telepora o player para outra cidade, ex: !h cerulean;
	!premium - Mostra as vantagens de ser VIP;
	!shinystone - Mostra a lista de shiny stones necessárias para evoluir cada shiny;
	!balls - Mostra as vantagens de cada ball especial. Utilize também: !heavy e !fast;
	!persistencia - Explica o Persistence Catch System;
	!aura - Altera a Aura de pokemons capturados pela Premier Ball;
	!eject - Expulsa da house players que não entram a mais de 7 dias;
	!walk - Se movimenta automaticamente;
	!buyhouse - Compra uma casa sem dono;
	!leave - Abandona sua casa (seus items vão para o depot);
	!online - Mostra os players online;
	!save - Salva seu personagem.;
	!bug - Use caso esteja preso/bugado;
	!bless - Checa se possui Bless ativo;
	!shinycharm - Checa quanto tempo resta do seu shiny charm.]])
	return true
end
